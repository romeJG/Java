    private void addBook(String f){
        
        try{
            File fl = new File(f);
            write(accnum,fl);
            write(",", fl);
            write(bn.getText(),fl);
            write(",", fl);
            write(txtName.getText(),fl);
            write(",", fl);
            write(txtAuth.getText(),fl);
            write(",", fl);
            write(txtCat.getText(),fl);
            write(",", fl);
            write("Available",fl);
            write("\n", fl);
            JOptionPane.showMessageDialog(null, "Book Added!");
            txtCat.setText(null);
            txtName.setText(null);
            txtAuth.setText(null);
            bn.setText(null);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error");
            
        }
    }
