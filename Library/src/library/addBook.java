    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nm = txtName.getText();
        String fourChars = "";
        
        int year = Calendar.getInstance().get(Calendar.YEAR);
        yr = String.valueOf(year);
        
        Random rand = new Random();     //start of making random number for the accession number
        int randomNumber = rand.nextInt(10000) + 10000;

        if (nm.length() > 4) 
            {
                fourChars = nm.substring(0, 4);
            } 
        else
            {
                fourChars = nm;
            }
        
        accnum = randomNumber + fourChars.toUpperCase();//makes the random accession number
        addBook("Books.txt");
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

