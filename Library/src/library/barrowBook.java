    private static void update(String f) {
        String tempFile = "temp.txt";
        File oldFile = new File(f);
        File newFile = new File(tempFile);
        String ann = "", bidd = "", tit = "", auth = "", cat = "", ava = "";
        try {
            FileWriter fw = new FileWriter(tempFile, true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter pw = new PrintWriter(bw);
            scan = new Scanner(new File(f));
            scan.useDelimiter("[,\n]");
            while (scan.hasNext()) {
                ann = scan.next();
                bidd = scan.next();
                tit = scan.next();
                auth = scan.next();
                cat = scan.next();// BABASAHIN YUNG FILE HABANG MAY LAMAN SIYA
                ava = scan.next();
                if (!bidd.equals(sbid.getText())) {
                    pw.println(ann + "," + bidd + "," + tit + "," + auth + "," + cat + "," + ava);
                }
            }
            pw.println(an.getText() + "," + bn.getText() + "," + txtName.getText() + "," + txtAuth.getText() + "," + txtCat.getText() + "," + "Unavailable");
            
            scan.close();
            pw.flush();
            pw.close();
            bw.close();
            fw.close();

            oldFile.delete();
            File dump = new File(f);
            newFile.renameTo(dump);

            JOptionPane.showMessageDialog(null, "Book Barrowed!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error Not Barrowed");
        }

    }
    private static void search() {
        try {
            File f = new File("Books.txt");
            FileReader fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);
            scan = new Scanner(f);
            String ann = "", bid = "", tit = "", auth = "", cat = "", ava = "";

            scan.useDelimiter("[,\n]");

            while (scan.hasNext()) {
                ann = scan.next();
                bid = scan.next();
                tit = scan.next();
                auth = scan.next();
                cat = scan.next();
                ava = scan.next();
                if (bid.equals(sbid.getText())) {
                    txtName.setText(tit);
                    txtAuth.setText(auth);
                    txtCat.setText(cat);
                    bn.setText(bid);
                    an.setText(ann);
                    ave.setText(ava);
                    tann = ann; tbid = bid; ttit = tit; tauth = auth; tcat = cat;
                    if ("Available".equals(ave.getText())){
                        ave.setBackground(new java.awt.Color(102, 255, 102));//green
                    }else{
                        ave.setBackground(new java.awt.Color(255, 51, 51));//red
                    }
                }
            }
            scan.close();
            br.close();
            fr.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "error showing books");
        }
        scan.close();
    }

