    public static void deleteRecord (String f, String s){
        String tempFile = "temp.txt";
        File oldFile = new File(f);
        File newFile = new File(tempFile);
        String an="",bidd="",tit="",auth="",cat="",ave="";
        try{
            FileWriter fw =new FileWriter(tempFile,true);
            BufferedWriter bw = new BufferedWriter(fw);
            PrintWriter pw = new PrintWriter(bw);
            scan = new Scanner(new File(f));
            scan.useDelimiter("[,\n]");
            
            while(scan.hasNext()){
                an=scan.next();
                bidd=scan.next();
                tit=scan.next();
                auth=scan.next();
                cat=scan.next();// BABASAHIN YUNG FILE HABANG MAY LAMAN SIYA
                ave=scan.next();
                
                if(!bidd.equals(s)){
                    pw.println(an+","+bidd+","+tit+","+auth+","+cat+","+ave);
                }
            }
            scan.close();
            pw.flush();
            pw.close();
            bw.close();
            fw.close();
            oldFile.delete();
            File dump = new File(f);
            newFile.renameTo(dump);
            
            JOptionPane.showMessageDialog(null, "Record deleted!");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "record Not deleted");
        }
        
        bid.setText(null);
        
    }

